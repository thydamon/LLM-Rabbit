def add(a: int, b: int) -> int:
    """ Add two integers.

    Args:
        a: The first integer to add.
        b: The second integer to add.
    """
    return a + b

def multiply(a: int, b: int) -> int:
    """ Multiply two integers.

    Args:
        a: The first integer to multiply.
        b: The second integer to multiply.
    """
    return a * b

tools = [add, multiply]
